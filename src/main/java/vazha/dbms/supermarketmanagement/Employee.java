    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String eid=jTextField1.getText();

        if(eid.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter values");
            return;
        }
        if(DataBase.db.DeleteEmp(eid)){
              JOptionPane.showMessageDialog(this, "Column deleted");
        }
        else JOptionPane.showMessageDialog(this, "Couldn't delete column");
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        for(Object a:DataBase.db.getEmployee()){
            model.addRow((Object [])a);
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String eid=jTextField1.getText();
        String efnm=jTextField2.getText();
        String elnm=jTextField3.getText();
        String eemail=jTextField4.getText();
        String epas=jTextField5.getText();
        String esal=jTextField6.getText();
        String eaid=jTextField7.getText();
       // TODO add your handling code here:
        
        if(eid.isEmpty() && efnm.isEmpty() && elnm.isEmpty() && eemail.isEmpty() && epas.isEmpty() && esal.isEmpty() && eaid.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter values");
            return;
        }
        if(DataBase.db.UpdateEmp(eid,efnm,elnm,eemail,epas,esal,eaid)){
            JOptionPane.showMessageDialog(this, "Column updated");
        }
        else JOptionPane.showMessageDialog(this, "Couldn't update column");
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        for(Object a:DataBase.db.getEmployee()){
            model.addRow((Object [])a);
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

